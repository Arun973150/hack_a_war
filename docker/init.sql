CFPB Final Rule: Defining Larger Participants in General-Use Digital Consumer Payment Applications
12 CFR Part 1090 Section 1090.109 | Effective January 9, 2025
SCOPE:
Applies to nonbank covered persons providing funds transfer or payment wallet functionalities for consumers' general use, including digital wallets, P2P payment apps, and mobile payment applications.
LARGER PARTICIPANT THRESHOLD:
Any nonbank entity facilitating 50 million or more annual consumer payment transactions denominated in USD qualifies as a larger participant subject to CFPB supervision. Affiliated company transactions are aggregated. Status lasts 2 years from the last qualifying tax year.
SUPERVISORY AUTHORITY:
CFPB may assess compliance with Federal consumer financial law, obtain records and documents, conduct on-site or remote examinations, test transactions for compliance, and issue examination reports and supervisory letters.
APPLICABLE LAWS:
1. CFPA UDAAP – prohibition against unfair, deceptive, or abusive acts (12 USC 5531, 5536)
2. GLBA Regulation P – privacy of nonpublic personal information (12 CFR Part 1016)
3. EFTA Regulation E – electronic fund transfer protections, including error resolution (12 CFR Part 1005)
COMPLIANCE REQUIREMENTS:
Entities must maintain compliance management systems. Examination covers compliance policies, processes, procedures, transaction testing, and documentation.
RISK AREAS IDENTIFIED:
• Consumer loss of funds or access to funds
• Fraud and protection gaps
• Uninsured stored value account risks
• Collection and use of consumer payments data
• Data security vulnerabilities
SERVICE PROVIDERS:
CFPB supervisory authority extends to service providers of covered persons.
CONSUMER PROTECTIONS:
Entities must comply with EFTA error resolution procedures, GLBA privacy notice requirements, and CFPA prohibition on unfair, deceptive, or abusive practices.

